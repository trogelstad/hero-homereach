  /* ───────────────────────────────────────────────
     BUILD 2: POSSIBILITY CALCULATOR
  ─────────────────────────────────────────────── */
  var calcPurchase = document.getElementById('calc-purchase-price');
  var calcMortgage = document.getElementById('calc-first-mortgage');
  var calcAssistance = document.getElementById('calc-assistance');
  var calcFuture = document.getElementById('calc-future-value');
  var calcAssistanceHint = document.getElementById('calc-assistance-hint');
  var calcCalculateBtn = document.getElementById('calc-calculate');
  var calcResults = document.getElementById('calc-results');
  var calcPrintBtn = document.getElementById('calc-print');
  var calcStarted = false;
  var assistanceManuallyEdited = false;

  var currencyFmt = new Intl.NumberFormat('en-US', { style: 'currency', currency: 'USD', maximumFractionDigits: 0 });
  var numberFmt = new Intl.NumberFormat('en-US', { maximumFractionDigits: 0 });

  function parseCurrency(el) {
    var raw = (el.value || '').replace(/[^0-9.]/g, '');
    var n = parseFloat(raw);
    if (isNaN(n) || n < 0) n = 0;
    return n;
  }

  function formatFieldValue(el, n) {
    el.value = numberFmt.format(Math.round(n));
  }

  function calcFireStarted() {
    if (calcStarted) return;
    calcStarted = true;
    dataLayer.push({ event: 'schools_to_home_calculator_started' });
  }

  [calcPurchase, calcMortgage, calcAssistance, calcFuture].forEach(function(el) {
    if (!el) return;
    el.addEventListener('focus', calcFireStarted);
  });

  if (calcAssistance) {
    calcAssistance.addEventListener('input', function() { assistanceManuallyEdited = true; });
  }

  function calcUpdateAssistanceCap() {
    var mortgage = parseCurrency(calcMortgage);
    var maxAssistance = mortgage * 0.25;
    calcAssistanceHint.textContent = 'Capped at 25% of your first-mortgage amount (currently ' + currencyFmt.format(Math.round(maxAssistance)) + ')';

    var currentAssistance = parseCurrency(calcAssistance);
    if (!assistanceManuallyEdited) {
      formatFieldValue(calcAssistance, maxAssistance);
    } else if (currentAssistance > maxAssistance) {
      formatFieldValue(calcAssistance, maxAssistance);
    }
  }

  if (calcMortgage) {
    calcMortgage.addEventListener('change', calcUpdateAssistanceCap);
    calcMortgage.addEventListener('blur', function() { formatFieldValue(calcMortgage, parseCurrency(calcMortgage)); calcUpdateAssistanceCap(); });
  }
  if (calcAssistance) {
    calcAssistance.addEventListener('blur', function() {
      var mortgage = parseCurrency(calcMortgage);
      var maxAssistance = mortgage * 0.25;
      var val = parseCurrency(calcAssistance);
      if (val > maxAssistance) val = maxAssistance;
      formatFieldValue(calcAssistance, val);
    });
  }
  if (calcPurchase) {
    calcPurchase.addEventListener('blur', function() { formatFieldValue(calcPurchase, parseCurrency(calcPurchase)); });
  }
  if (calcFuture) {
    calcFuture.addEventListener('blur', function() { formatFieldValue(calcFuture, parseCurrency(calcFuture)); });
  }

  function calcRunCalculation() {
    var purchasePrice = parseCurrency(calcPurchase);
    var firstMortgage = parseCurrency(calcMortgage);
    var futureValue = parseCurrency(calcFuture);
    var potentialMax = firstMortgage * 0.25;
    var assistanceUsed = parseCurrency(calcAssistance);
    if (assistanceUsed > potentialMax) assistanceUsed = potentialMax;

    var assistancePct = purchasePrice > 0 ? (assistanceUsed / purchasePrice) : 0;
    var appreciation = Math.max(futureValue - purchasePrice, 0);
    var sharedAppreciation = assistancePct * appreciation;
    var totalPayoff = assistanceUsed + sharedAppreciation;

    document.getElementById('calc-r-max').textContent = currencyFmt.format(Math.round(potentialMax));
    document.getElementById('calc-r-assistance').textContent = currencyFmt.format(Math.round(assistanceUsed));
    document.getElementById('calc-r-pct').textContent = (assistancePct * 100).toFixed(1) + '%';
    document.getElementById('calc-r-appreciation').textContent = currencyFmt.format(Math.round(appreciation));
    document.getElementById('calc-r-shared').textContent = currencyFmt.format(Math.round(sharedAppreciation));
    document.getElementById('calc-r-payoff').textContent = currencyFmt.format(Math.round(totalPayoff));

    var interpretation = 'At this example, assistance of ' + currencyFmt.format(Math.round(assistanceUsed)) + ' could represent a major change in the upfront-cost conversation. It may shorten the time needed to save, expand the communities worth exploring or make buying closer to work feel more realistic.';
    document.getElementById('calc-interpretation').textContent = interpretation;

    var repaymentNote;
    if (appreciation <= 0) {
      repaymentNote = 'In this example, there is no positive appreciation to share. The original assistance remains part of the long-term repayment plan.';
    } else {
      repaymentNote = 'In return, the original assistance remains part of the long-term repayment plan, along with the estimated shared-appreciation amount shown above.';
    }
    document.getElementById('calc-repayment-note').textContent = repaymentNote;

    calcResults.hidden = false;

    dataLayer.push({
      event: 'schools_to_home_calculator_completed',
      purchase_price: Math.round(purchasePrice / 1000) * 1000,
      first_mortgage: Math.round(firstMortgage / 1000) * 1000,
      modeled_assistance: Math.round(assistanceUsed / 1000) * 1000,
      future_value: Math.round(futureValue / 1000) * 1000,
      estimated_appreciation_share: Math.round(sharedAppreciation / 100) * 100
    });

    return { calculated: true };
  }

  if (calcCalculateBtn) {
    calcCalculateBtn.addEventListener('click', function() {
      calcUpdateAssistanceCap();
      calcRunCalculation();
    });
  }

  if (calcPrintBtn) {
    calcPrintBtn.addEventListener('click', function() {
      if (calcResults.hidden) {
        calcUpdateAssistanceCap();
        calcRunCalculation();
      }
      dataLayer.push({ event: 'schools_to_home_estimate_printed' });
      window.print();
    });
  }

  // initialize default cap/hint on load
  calcUpdateAssistanceCap();

